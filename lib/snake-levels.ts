function safeRows(rows: string[]): string[] {
  return rows.map((r) => r.padEnd(20, ".").slice(0, 20));
}

export function walls(level: number): Pt[] {
  const idx = (level - 1) % RAW.length;
  const rows = safeRows(RAW[idx]);
  const out: Pt[] = [];
  for (let y = 0; y < GRID; y++) {
    for (let x = 0; x < GRID; x++) {
      if (rows[y][x] === "#") out.push({ x, y });
    }
  }
  return out;
}

export function speedMs(level: number): number {
  // 130ms at level 1 down to ~40ms at level 50
  return Math.max(38, 135 - level * 2);
}

// dots needed to complete the level (small at start, big later)
export function goalFor(level: number): number {
  return Math.min(40, 5 + level);
}
